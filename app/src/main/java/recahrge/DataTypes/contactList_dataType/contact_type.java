package recahrge.DataTypes.contactList_dataType;

public class contact_type {

    public String name;
    public String number;

    public contact_type(String name, String number) {
        this.name = name;
        this.number =  number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

}
